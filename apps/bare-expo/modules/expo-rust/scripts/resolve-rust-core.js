#!/usr/bin/env node
/**
 * Resolves the actual filesystem path of the expo-modules-rs Rust crate,
 * regardless of package manager layout (npm, yarn, pnpm, bun).
 *
 * This script is invoked by the platform build scripts (Gradle, CocoaPods)
 * before running `cargo build`. It:
 *
 * 1. Uses require.resolve() to find the core package (works with pnpm/bun symlinks)
 * 2. Writes .cargo/config.toml with [patch.crates-io] pointing to the resolved path
 * 3. Exits with an error if the core package cannot be found
 *
 * Usage:
 *   node scripts/resolve-rust-core.js [--print-path]
 *
 * With --print-path: prints the resolved rust/ directory path and exits (for CMake)
 * Without flags: writes .cargo/config.toml and prints the path
 */

const path = require('path');
const fs = require('fs');

function findRepoRoot(startDir) {
  let dir = startDir;
  while (dir !== path.dirname(dir)) {
    if (
      fs.existsSync(path.join(dir, 'pnpm-workspace.yaml')) ||
      fs.existsSync(path.join(dir, '.git'))
    ) {
      return dir;
    }
    dir = path.dirname(dir);
  }
  return null;
}

function resolveCorePath() {
  // Try to resolve the package.json of expo-modules-rs
  // This works with all package managers because require.resolve
  // follows symlinks and handles .pnpm stores correctly.
  const strategies = [
    // Strategy 1: require.resolve from the package root
    () => {
      const pkgJson = require.resolve('expo-modules-rs/package.json');
      return path.join(path.dirname(pkgJson), 'rust');
    },
    // Strategy 2: Walk up to the monorepo root and look in packages/
    () => {
      const repoRoot = findRepoRoot(__dirname);
      if (!repoRoot) throw new Error('Not in a monorepo');
      const candidate = path.join(repoRoot, 'packages', 'expo-modules-rs', 'rust');
      if (fs.existsSync(path.join(candidate, 'Cargo.toml'))) {
        return candidate;
      }
      throw new Error('Not found in monorepo packages');
    },
  ];

  for (const strategy of strategies) {
    try {
      const resolved = strategy();
      // Verify the resolved path contains a valid Cargo.toml
      const cargoToml = path.join(resolved, 'Cargo.toml');
      if (fs.existsSync(cargoToml)) {
        return resolved;
      }
    } catch {
      // Try next strategy
    }
  }

  return null;
}

function writeCargoConfig(corePath) {
  const packageRoot = path.resolve(__dirname, '..');
  const cargoDir = path.join(packageRoot, '.cargo');
  const configPath = path.join(cargoDir, 'config.toml');

  // Ensure .cargo directory exists
  fs.mkdirSync(cargoDir, { recursive: true });

  // Path to expo-module-macro within the core package
  const macroPath = path.join(corePath, 'expo-module-macro');

  // Write the config with [patch.crates-io] to override the crates.io dependency
  // with the locally-resolved npm package path. This works even if the crate
  // hasn't been published to crates.io yet.
  const config = `# Auto-generated by resolve-rust-core.js
# This file patches the expo-modules-rs crates.io dependency to point
# to the locally-installed npm package. Do not edit manually.
#
# Regenerated on every build by the platform build scripts.

[patch.crates-io]
expo-modules-rs = { path = "${corePath.replace(/\\/g, '/')}" }
expo-module-macro = { path = "${macroPath.replace(/\\/g, '/')}" }
`;

  fs.writeFileSync(configPath, config, 'utf8');
  return configPath;
}

function verifyCargoCanResolve(corePath) {
  // Quick sanity check: verify the Cargo.toml has the expected crate name
  const cargoToml = fs.readFileSync(path.join(corePath, 'Cargo.toml'), 'utf8');
  if (!cargoToml.includes('name = "expo-modules-rs"')) {
    console.error(
      `error: Found Cargo.toml at ${corePath} but it does not define expo-modules-rs`
    );
    process.exit(1);
  }

  // Verify expo-module-macro exists
  const macroCargoToml = path.join(corePath, 'expo-module-macro', 'Cargo.toml');
  if (!fs.existsSync(macroCargoToml)) {
    console.error(
      `error: expo-module-macro not found at ${macroCargoToml}`
    );
    process.exit(1);
  }
}

// Main
const printPathOnly = process.argv.includes('--print-path');

const corePath = resolveCorePath();

if (!corePath) {
  console.error(`
error: Could not resolve expo-modules-rs.

The Rust SDK package is required to build expo-rust.
Install it with your package manager:

  pnpm add expo-modules-rs
  # or
  npm install expo-modules-rs

For monorepo development, ensure expo-modules-rs is in packages/.
`);
  process.exit(1);
}

verifyCargoCanResolve(corePath);

if (printPathOnly) {
  // Just print the path (used by CMake/Gradle for include paths)
  process.stdout.write(corePath);
} else {
  // Write .cargo/config.toml and print status
  const configPath = writeCargoConfig(corePath);
  console.log(`expo-modules-rs resolved: ${corePath}`);
  console.log(`Cargo config written: ${configPath}`);
}
